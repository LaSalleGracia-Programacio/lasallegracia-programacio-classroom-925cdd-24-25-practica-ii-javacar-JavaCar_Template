package org.JavaCar;

public class Roda {
    private String tipus;
    private int potencia;

    public Roda(String tipus, int potencia) {
        this.tipus = tipus;
        this.potencia = potencia;
    }
    public String getTipus() {
        return tipus;
    }
    public int getPotencia() {
        return potencia;
    }
}
