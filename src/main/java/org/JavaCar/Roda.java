package org.JavaCar;

public class Roda {

    private String marca;
    private double potencia;

    public Roda(String marca, double potencia) {
        this.marca = marca;
        this.potencia = potencia;
    }

    public String getMarca() {
        return marca;
    }
    public double getPotencia() {
        return potencia;
    }


}
